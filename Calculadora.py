a=int(input("Ingrese un numero: "))
b=int(input("Ingrese un segundo numero: "))

def suma():
    resultado=a+b
    print("Su suma es: ", resultado)

def resta():
    resultado=a-b
    print("Su resta es: ", resultado)

def multiplicacion():
    resultado=a*b
    print("Su multiplicacion es: ", resultado)

def division():
    resultado=a/b
    print("Su division es: ", resultado)

suma()
resta()
multiplicacion()
division()